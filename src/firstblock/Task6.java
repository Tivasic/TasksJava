package firstblock;

public class Task6 {
    public static int getAscii(char sym) {
        return sym;
    }

    public static void main(String[] args) {
        System.out.println("1. ASCII код символа: " + getAscii('A'));
        System.out.println("2. ASCII код символа: " + getAscii('m'));
        System.out.println("3. ASCII код символа: " + getAscii('['));
        System.out.println("4. ASCII код символа: " + getAscii('\\'));
    }
}
